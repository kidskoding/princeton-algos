#include <iostream>
#include <string>

void printHello(std::string first, std::string second) {
    std::cout << "Hello" << first << " and " << second << "." << std::endl;
}

void printGoodbye(std::string first, std::string second) {
    std::cout << "Goodbye" << second << " and " << first << "." << std::endl;
}